    public void setModel(DienThoai dt){
        TextMa2.setText(dt.getMa());
        TextTen2.setText(dt.getTen());
        TextSoLuong2.setText(String.valueOf(dt.getSoluong()));
        TextGia2.setText(String.valueOf(dt.getGia()));
        TextTong2.setText(String.valueOf(dt.Tong()));
        String valueHang=dt.getHang();
        jcHang2.setSelectedItem(valueHang);
        if(dt.getHinhanh().equals("No avatar")){
            hinhanh2.setText("No avatar");
            hinhanh2.setIcon(null);
        }
            
        
        else{
            hinhanh2.setIcon(ResizeImage(String.valueOf(dt.getHinhanh())));
        }
    }
    public DienThoai getModel(){
        DienThoai dt=new DienThoai();
        dt.setGia(Long.parseLong(TextGia2.getText()));
        dt.setHang((String)jcHang2.getSelectedItem());
        dt.setMa(TextMa2.getText());
        dt.setSoluong(Long.parseLong(TextSoLuong2.getText()));
        dt.setTen(TextTen2.getText());
        if(strHinhAnh==null){
            dt.setHinhanh("No avatar");
        }
        else{
            dt.setHinhanh(strHinhAnh);
        }
        return dt;
    }
    
    public void fillTable(){
        DefaultTableModel table=(DefaultTableModel) jtDienThoai2.getModel();
        table.setRowCount(0);
        for(DienThoai dt:list.getDienThoai()){
            Object rowjtDienThoai[]=new Object[6];
            rowjtDienThoai[0]=dt.getMa();
            rowjtDienThoai[1]=dt.getTen();
            rowjtDienThoai[3]=String.valueOf(dt.getGia());
            rowjtDienThoai[2]=dt.getHang();
            rowjtDienThoai[4]=String.valueOf(dt.getSoluong());
            rowjtDienThoai[5]=dt.getHinhanh();
        
            table.addRow(rowjtDienThoai);
        }
        
        
    }
