    public KhachHang getModelKH(){
        KhachHang kh=new KhachHang();
        kh.setTen(TextTenKhachHang.getText());
        kh.setSdt(TextSDT.getText());
        kh.setDiachi(TextDiaChi.getText());
        kh.setTendt((String)jcTen.getSelectedItem());
        kh.setHang(TextHang.getText());
        kh.setBaohanh((String)jcBaoHanh.getSelectedItem());
        kh.setThanhtien(Long.parseLong(TextSoLuong.getText())*Long.parseLong(TextGia.getText()));
        SimpleDateFormat sd = new SimpleDateFormat("dd/MM/yyyy");
        String date=sd.format(jDate.getDate());
        kh.setThoigian(date);
        kh.setSoluong(Long.parseLong(TextSoLuong.getText()));
        return kh;
    }
    
